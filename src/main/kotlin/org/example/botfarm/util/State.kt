package org.example.botfarm.util

/**
 * Enumeration representing possible states of an application or a component.
 *
 * This enum defines two states: [DEFAULT] and [WEATHER].
 * - [DEFAULT]: The default state or initial state of the application/component.
 * - [WEATHER]: A specific state related to weather information or functionality.
 */
enum class State {
    DEFAULT,
    WEATHER,
}
